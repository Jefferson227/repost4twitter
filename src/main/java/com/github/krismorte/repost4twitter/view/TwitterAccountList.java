    private void showTable(List list) {
        helpTable.setList(list);
        helpTable.setJPanel(panelList);
        table = helpTable.show();
        table.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() >= 2) {
                    TwitterAccountScreen twitterAccount = new TwitterAccountScreen(helpTable.getSelected());
                    twitterAccount.setup();
                    showTable(twitterAccountDao.list());
                }
            }
        });
    }

